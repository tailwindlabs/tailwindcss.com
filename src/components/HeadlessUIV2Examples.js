export function ComboboxExample() {
  const [query, setQuery] = useState('')
  const [selected, setSelected] = useState(people[0])

  const filteredPeople =
    query === ''
      ? people
      : people.filter((person) => {
          return person.name.toLowerCase().includes(query.toLowerCase())
        })

  return (
    <Example className="h-[395px]" hint="Open the combobox and scroll through the 1,000 options">
      <Field className="mx-auto max-w-xs">
        <Label className="block select-none text-sm/6 font-medium text-zinc-900 dark:text-white">
          Select user
        </Label>
        <Combobox
          value={selected}
          virtual={{ options: filteredPeople }}
          onChange={(value) => setSelected(value)}
        >
          <div className="mt-1 relative">
            <ComboboxInput
              className="block w-full rounded-md border border-slate-300 backdrop-blur bg-white text-zinc-900 dark:text-white dark:bg-white/5 dark:border-white/10 px-3 py-2 placeholder-slate-400 shadow-sm data-[focus]:border-sky-500 dark:data-[focus]:border-sky-500 data-[focus]:outline-none data-[focus]:ring-1 data-[focus]:ring-sky-500 sm:text-sm"
              displayValue={(person) => person?.name}
              onChange={(event) => setQuery(event.target.value)}
            />
            <ComboboxButton className="group absolute inset-y-0 right-0 px-2.5">
              <ChevronDownIcon className="size-4 fill-slate-400 dark:fill-white/60 dark:group-data-[hover]:fill-white" />
            </ComboboxButton>
          </div>
          <Transition
            leave="transition ease-in duration-100"
            leaveFrom="opacity-100"
            leaveTo="opacity-0"
            afterLeave={() => setQuery('')}
          >
            <ComboboxOptions className="mt-2 max-h-60 w-[--input-width] overflow-y-auto rounded-xl border bg-white dark:border-white/5 dark:bg-white/5 backdrop-blur p-1 empty:invisible">
              {({ option: person }) => (
                <ComboboxOption
                  key={person.id}
                  value={person}
                  className="w-full group flex cursor-default items-center gap-2 rounded-lg py-1.5 px-3 select-none data-[focus]:bg-zinc-100 dark:data-[focus]:bg-white/5"
                >
                  <CheckIcon className="invisible size-4 fill-slate-900 dark:fill-white group-data-[selected]:visible" />
                  <div className="text-sm/6 text-slate-800 dark:text-white">{person.name}</div>
                </ComboboxOption>
              )}
            </ComboboxOptions>
          </Transition>
        </Combobox>
      </Field>
    </Example>
  )
}

