export function ComboboxExample() {
  const [query, setQuery] = useState("");
  const [selected, setSelected] = useState(people[0]);

  const filteredPeople =
    query === ""
      ? people
      : people.filter((person) => {
          return person.name.toLowerCase().includes(query.toLowerCase());
        });

  return (
    <Example className="h-[395px] dark:bg-gray-950/50!">
      <Field className="mx-auto max-w-xs">
        <Label className="block text-sm/6 font-medium text-zinc-900 select-none dark:text-white">Select user</Label>
        <Combobox value={selected} virtual={{ options: filteredPeople }} onChange={(value: any) => setSelected(value)}>
          <div className="relative mt-1">
            <ComboboxInput
              className="block w-full rounded-md border border-slate-300 bg-white px-3 py-2 text-zinc-900 placeholder-slate-400 shadow-sm backdrop-blur data-[focus]:border-sky-500 data-[focus]:ring-1 data-[focus]:ring-sky-500 data-[focus]:outline-none sm:text-sm dark:border-white/10 dark:bg-white/5 dark:text-white dark:data-[focus]:border-sky-500"
              displayValue={(person: any) => person?.name}
              onChange={(event) => setQuery(event.target.value)}
            />
            <ComboboxButton className="group absolute inset-y-0 right-0 px-2.5">
              <ChevronDownIcon className="size-4 fill-slate-400 dark:fill-white/60 dark:group-data-[hover]:fill-white" />
            </ComboboxButton>
          </div>
          <Transition
            leave="transition ease-in duration-100"
            leaveFrom="opacity-100"
            leaveTo="opacity-0"
            afterLeave={() => setQuery("")}
          >
            <ComboboxOptions className="mt-2 max-h-60 w-(--input-width) overflow-y-auto rounded-xl border border-gray-200 bg-white p-1 backdrop-blur empty:invisible dark:border-white/5 dark:bg-white/5">
              {({ option: person }) => (
                <ComboboxOption
                  key={person.id}
                  value={person}
                  className="group flex w-full cursor-default items-center gap-2 rounded-lg px-3 py-1.5 select-none data-[focus]:bg-zinc-100 dark:data-[focus]:bg-white/5"
                >
                  <CheckIcon className="invisible size-4 fill-slate-900 group-data-[selected]:visible dark:fill-white" />
                  <div className="text-sm/6 text-slate-800 dark:text-white">{person.name}</div>
                </ComboboxOption>
              )}
            </ComboboxOptions>
          </Transition>
        </Combobox>
      </Field>
    </Example>
  );
}

